package com.dsan.web.qm.vo;

public class RuleVO {
	private String rule_id;
	private String rule_nm;
	private String rule_cn;
	private String rule_desc;
	private String rule_se;

	public String getRule_id() {
		return rule_id;
	}

	public void setRule_id(String rule_id) {
		this.rule_id = rule_id;
	}

	public String getRule_nm() {
		return rule_nm;
	}

	public void setRule_nm(String rule_nm) {
		this.rule_nm = rule_nm;
	}

	public String getRule_cn() {
		return rule_cn;
	}

	public void setRule_cn(String rule_cn) {
		this.rule_cn = rule_cn;
	}

	public String getRule_desc() {
		return rule_desc;
	}

	public void setRule_desc(String rule_desc) {
		this.rule_desc = rule_desc;
	}

	public String getRule_se() {
		return rule_se;
	}

	public void setRule_se(String rule_se) {
		this.rule_se = rule_se;
	}
}